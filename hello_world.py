# Various ways to show "Hello World"

# Common print statement
print("Hello World!")

# Combining indivdual characters
print("H" + "e" + "l" + "l" + "o" + " " + "W" + "o" + "r" + "l" + "d" + "!")

# Assigning the greeting with a single variable
x = "Hello World!"
print(x)

# Breaking the greeting into two parts and then combining them
a = "Hello "
b = "World"
c = "!"
print(a+b+c)